export default class Contacto {

    constructor(
        id_contacto,
        nombre,
        apellido,
        nombre_categoria,
        tipo_dato,
        valor
    ) {

        this.id_contacto = id_contacto;
        this.nombre = nombre;
        this.apellido = apellido;
        this.nombre_categoria = nombre_categoria;
        this.tipo_dato = tipo_dato;
        this.valor = valor;

    }

}